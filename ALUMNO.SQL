CREATE DATABASE ALUMNO;

USE ALUMNO;


CREATE TABLE CARRERA (
    NoCarrera int,
    Carrera VARCHAR(255),
    constraint pk_cc PRIMARY KEY(NoCarrera);
);

CREATE TABLE MATERIA(
    NumMateria int,
    Materia VARCHAR(255),
    constraint pk_cm PRIMARY KEY(NumMateria)
);

CREATE TABLE DELEGACION(
    Id_Delegacion int,
    Delegacion VARCHAR(255),
    constraint pk_cd PRIMARY KEY(Id_Delegacion)
);

CREATE TABLE TELEFONO(
    Id_tipotel int,
    tipotel VARCHAR(255),
    constraint pk_ct PRIMARY KEY(Id_tipotel)
);



CREATE TABLE COLONIA(
    ID_COLONIA INT,
    COLONIA VARCHAR(255),
    ID_DELEGACION INT,
    CONSTRAINT PK_COL PRIMARY KEY(ID_COLONIA),
    CONSTRAINT FK_DEL FOREIGN KEY(ID_DELEGACION) REFERENCES DELEGACION(Id_Delegacion)
);

CREATE TABLE CALLE(
    ID_CALLE INT,
    CALLE VARCHAR(255),
    ID_COLONIA2 INT,
    CONSTRAINT PK_CCALL PRIMARY KEY(ID_CALLE),
    CONSTRAINT FK_CCOL FOREIGN KEY(ID_COLONIA2) REFERENCES COLONIA(ID_COLONIA)
);

CREATE TABLE ALUMNO(
    MATRICULA_ALU INT,
    NOMBRE_ALU VARCHAR,
    APP_ALU INT,
    APM_ALU INT,
    NUM_INT INT,
    NUM_EXT INT,
    CP INT,
    ID_DELEGACION1 INT,
    ID_COLONIA1 INT,
    ID_CALLE1 INT,
    NOCARRERA1 INT,
    CONSTRAINT PK_CMAT PRIMARY KEY(MATRICULA_ALU),
    CONSTRAINT FK_CIDDEL FOREIGN KEY(ID_DELEGACION1),
    CONSTRAINT FK_CIDCOL FOREIGN KEY(ID_COLONIA1),
    CONSTRAINT FK_CIDCAL FOREIGN KEY(ID_CALLE1),
    CONSTRAINT FK_CNCAR FOREIGN KEY(NOCARRERA1),
);

CREATE TABLE ALUMNO_TELEFONO(
    ID_TIPOTEL1 INT,
    MATRICULA_ALU2 INT,
    TELEFONO BIGINT,
    CONSTRAINT PK_CTELE PRIMARY KEY(TELEFONO),
    CONSTRAINT FK_CMAALU2 FOREIGN KEY(MATRICULA_ALU2),
    CONSTRAINT FK_CIDTITEL2 FOREIGN KEY(ID_TIPOTEL1),
);

CREATE TABLE ALUMNO_MATERIA(
    MATRICULA_ALU1 INT,
    NUMMATERIA1 INT,
    CONSTRAINT FK_CMAALU1 FOREIGN KEY(MATRICULA_ALU1),
    CONSTRAINT FK_CMATERIA1 FOREIGN KEY(NUMMATERIA1),
);